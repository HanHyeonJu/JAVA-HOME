package application;

public class Final {

	public static void main(String[] args) {
		// final 붙은 변수는 상수가 된다
		// 상수 = 변하지 않는 수
		
		final int VALUE = 9;
//		VALUE = 10;
		
		System.out.println(VALUE);
		
		final String USER_PASSWORD = "abc";
//		USER_PASSWORD = "ABC"
				
		System.out.println(USER_PASSWORD);

	}

}
