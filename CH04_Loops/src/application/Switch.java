package application;

public class Switch {

	public static void main(String[] args) {
		int option = 0;
		
		switch(option) {
		case 0:
			System.out.println("可记 0 急琶.");
			break;
		case 1:
			System.out.println("可记 1 急琶.");
			break;
		case 10:
			System.out.println("可记 10 急琶.");
			break;
		default:
			System.out.println("肋给等 可记锅龋.");
			break;
		}

	}

}
